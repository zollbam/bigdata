public class RunStatementExample {
    //프로그램 실행 진입점
    public static void main(String[] args){
        // 변수 설정
        int x = 1; int y = 2; // ;으로 구분이 되기 때문에 한줄에 여러개의 실행문 작성 가능
        // 식 만들기
        int result 
        = x + y; // ;이 실행문의 끝을 나타내주므로 여러줄을 이용한 작성도 가능
        // 결과 출력
        System.out.println(result);
    }
}
